package com.codegnan.patterns;

public class PatternDemo_02 {

	public static void main(String[] args) {
		int rows=10,cols=10;
		//outer for loop talks about rows
		for(int i=0;i<rows;i++) {
			//inner for loop talks about columns
			for (int j=0;j<cols;j++) {
				//print star
				System.out.print(j+"");		
				
			}
		 	System.out.println();
		}
		
	}
}
		
		

	


